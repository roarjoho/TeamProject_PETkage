package domain;

public class ProductCategoryDTO {
	private int productCategory_number;
	private String productCategory_name;
	
	public int getProductCategory_number() {
		return productCategory_number;
	}
	public void setProductCategory_number(int productCategory_number) {
		this.productCategory_number = productCategory_number;
	}
	public String getProductCategory_name() {
		return productCategory_name;
	}
	public void setProductCategory_name(String productCategory_name) {
		this.productCategory_name = productCategory_name;
	}
	
	
}
